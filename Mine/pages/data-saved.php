let allData = [];

// Fungsi untuk mengumpulkan data dari semua halaman
function fetchAllData() {
    allData = []; // Kosongkan data sebelumnya

    // Ambil total halaman dari pagination
    const totalPages = document.querySelectorAll(".pagination .page-item").length - 2;
    let fetchPromises = [];

    // Loop melalui semua halaman dan ambil datanya
    for (let i = 1; i <= totalPages; i++) {
        const promise = fetch(`getVoicesAll.php?page=${i}`)
            .then(response => response.json())
            .then(responseData => {
                if (responseData.data && Array.isArray(responseData.data)) {
                    allData = allData.concat(responseData.data);
                }
            })
            .catch(error => {
                console.error(`Error fetching data from page ${i}:`, error);
            });

        fetchPromises.push(promise);
    }

    // Tunggu sampai semua data terkumpul
    return Promise.all(fetchPromises);
}

// Fungsi untuk melakukan pencarian di semua data
function searchTable() {
    const input = document.getElementById("searchInput");
    const filter = input.value.toLowerCase().trim();

    if (filter === "") {
        // Jika input kosong, kembalikan ke pagination normal
        loadTableData(1);
        return;
    }

    // Filter data berdasarkan input pencarian
    const filteredData = allData.filter(item => {
        return (
            (item.voice && item.voice.toLowerCase().includes(filter)) ||
            (item.pass_voice && item.pass_voice.toLowerCase().includes(filter)) ||
            (item.serial_number && item.serial_number.toLowerCase().includes(filter)) ||
            (item.created_at && item.created_at.toLowerCase().includes(filter))
        );
    });

    // Tampilkan hasil pencarian
    renderTable(filteredData);
    updatePagination(1); // Nonaktifkan pagination saat pencarian
}

// Event listener untuk input pencarian
document.getElementById("searchInput").addEventListener("input", function() {
    fetchAllData().then(() => {
        searchTable();
    });
});


