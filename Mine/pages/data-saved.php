  let currentPage = 1;

// Fungsi untuk menampilkan data di tabel
function renderTable(data) {
    console.log("Rendering data:", data);  // Debugging: cek apakah data diterima dengan benar

    const tableBody = document.getElementById("voiceall");
    if (!tableBody) {
        console.error("Element with id not found!");
        return;
    }

    tableBody.innerHTML = ""; // Bersihkan tabel sebelumnya

    if (data.length === 0) {
        console.log("No data to display.");
        tableBody.innerHTML = `<tr><td colspan="5" class="text-center">No data found</td></tr>`;
        return;
    }

    data.forEach((item, index) => {
        const row = document.createElement("tr");
        row.innerHTML = `
            <td class="text-xs font-weight-bold" style="padding-left:28px;">${index + 1}</td>
            <td class="text-xs font-weight-bold">${item.voice || "N/A"}</td>
            <td class="text-center text-xs font-weight-bold">${item.pass_voice || "N/A"}</td>
            <td class="text-center text-xs font-weight-bold">${item.serial_number || "N/A"}</td>
            <td class="text-center text-xs font-weight-bold">${item.created_at || "N/A"}</td>
        `;
        tableBody.appendChild(row);
    });
}

// Fungsi mengambil data dari API dan render tabel
function loadTableData(page = 1) {
    fetch(`getVoicesAll.php?page=${page}`)
        .then(response => response.json())
        .then(responseData => {
            console.log("Data received from getVoicesAll.php:", responseData);
            
            if (responseData.data && Array.isArray(responseData.data) && responseData.data.length > 0) {
                renderTable(responseData.data);
                updatePagination(responseData.totalPages);
            } else {
                console.log("No data found or invalid data format.");
                document.getElementById("voiceall").innerHTML = `<tr><td colspan="5" class="text-center">No data found</td></tr>`;
                updatePagination(0);  // Bersihkan pagination jika data kosong
            }
        })
        .catch(error => {
            console.error("Error fetching data from API:", error);
        });
}

// Fungsi update pagination
function updatePagination(totalPages) {
    const paginationContainer = document.getElementById("pagination");
    paginationContainer.innerHTML = '';

    if (totalPages <= 1) return; // Tidak perlu pagination jika hanya ada 1 halaman

    const ul = document.createElement("ul");
    ul.classList.add("pagination", "justify-content-center");

    // Tombol Previous
    const prevLi = document.createElement("li");
    prevLi.classList.add("page-item");
    if (currentPage === 1) prevLi.classList.add("disabled");
    prevLi.innerHTML = `
        <a class="page-link" href="#" aria-label="Previous" onclick="changePage(${currentPage - 1})">
            <span aria-hidden="true">&laquo;</span>
        </a>`;
    ul.appendChild(prevLi);

    // Tombol Angka
    for (let i = 1; i <= totalPages; i++) {
        const li = document.createElement("li");
        if (i === currentPage) {
        li.classList.add("page-item", "active");
    } else {
    li.classList.add("page-item");
    }

        li.innerHTML = `
            <a class="page-link" href="#" onclick="changePage(${i})">${i}</a>`;
        ul.appendChild(li);
    }

    // Tombol Next
    const nextLi = document.createElement("li");
    nextLi.classList.add("page-item");
    if (currentPage === totalPages) nextLi.classList.add("disabled");
    nextLi.innerHTML = `
        <a class="page-link" href="#" aria-label="Next" onclick="changePage(${currentPage + 1})">
            <span aria-hidden="true">&raquo;</span>
        </a>`;
    ul.appendChild(nextLi);

    paginationContainer.appendChild(ul);
}


// Fungsi ganti halaman
function changePage(newPage) {
    if (newPage >= 1) {
        currentPage = newPage;
        loadTableData(currentPage);
    }
}

// Memanggil data saat halaman dimuat
window.onload = function() {
    console.log("Page loaded. Loading table data...");
    loadTableData(currentPage);
};

